Map::Map()
{
    dirt = TextureManager::LoadTexture("assets/maps/tileset/1.png");
    bush = TextureManager::LoadTexture("assets/maps/tileset/10.png");

    LoadMap(lv1);

    src.x = src.y = 0;
    src.w = src.h = 64;
    dest.x = dest.y = 0;
    dest.w = dest.h = 64;
}

Map::~Map()
{

}

void Map::LoadMap(int arr[20][20])
{
    for(int row = 0; row < 20; ++row)
    {
        for(int col = 0; col < 20; ++col)
        {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::DrawMap()
{
    int type = 0;

    for(int row = 0; row < 20; ++row)
    {
        for(int col = 0; col < 20; ++col)
        {
            type = map[row][col];

            dest.x = col*64 - Game::camera.x;
            dest.y = row*64 - Game::camera.y;

            switch(type)
            {
            case 0:
                TextureManager::Draw(dirt, src, dest);
                break;
            case 1:
                TextureManager::Draw(bush, src, dest);
            }
        }
    }
}
